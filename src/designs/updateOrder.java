    void addvaluesorder() {
        ArrayList <Order> objects = store.printallorder();
        if (objects != null) {
            DefaultTableModel model = (DefaultTableModel) ordertabel.getModel();
             model.setRowCount(0);
            for (int i = 0; i < objects.size(); i++) {
                Object[] colarr = new Object[4];
                colarr[0] = objects.get(i).getOrderId();
                colarr[1] = objects.get(i).getCustomerId();
                colarr[2] = objects.get(i).getCustomerName();
                colarr[3] = objects.get(i).getCustomerPhonenumber();
                model.addRow(colarr);
            }
        }
    }


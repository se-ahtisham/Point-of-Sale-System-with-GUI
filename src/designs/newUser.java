    private void passwordMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_passwordMouseClicked
        // TODO add your handling code here:
        if (!email.getText().equals("Email")) {
            if (Pattern.compile("[a-zA-Z0-9]+@gmail.com").matcher(email.getText()).matches()) {
                boolean result = store.checkuseremail(email.getText());
                if (result) {
                    store.showerrormessage("Email Already Register");
                } else {
                    if (new String(password.getPassword()).equals("Password")) {
                        password.setText("");
                        password.setForeground(Color.BLACK);
                    }
                }
            } else {
                store.showerrormessage("Wronge Email!");
                email.setText("");
            }
        } else {
            store.showerrormessage("Enter Email");
        }
    }//GEN-LAST:event_passwordMouseClicked


    private void jTextField23MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTextField23MouseClicked
        // TODO add your handling code here:

        if ((Pattern.compile("[a-zA-Z0-9]+@gmail.com").matcher(email.getText()).matches()) && !email.getText().equals("")) {
            userclass user = new userclass();
            user.setUserEmail(email.getText());
            if (!new String(password.getPassword()).equals("Password") && !new String(password.getPassword()).equals("")) {
                user.setUserPassword(new String(password.getPassword()));
                if (!name.getText().equals("Name") && !name.getText().equals("")) {
                    user.setUserName(name.getText());                   
                    if (!question.getSelectedItem().toString().equals("Set Security Question")) {
                        user.setUserQuestion(question.getSelectedItem().toString());
                        if (!answer.getText().equals("Security Answer") && !answer.getText().equals("")) {
                            user.setUserAnswer(answer.getText());
                            boolean result = store.adduser(user);
                            if (result) {
                                store.showerrormessage("User has been added");
                                email.setText("");
                                password.setText("");
                                name.setText("");
                                question.setSelectedItem("Set Security Question");
                                answer.setText("");

                            } else {
                                store.showerrormessage("Data not received!");
                            }
                        } else {
                            store.showerrormessage("Answer Error!");
                        }

                    } else {
                        store.showerrormessage("Question Error!");
                    }

                } else {
                    store.showerrormessage("Name Error!");
                }

            } else {
                store.showerrormessage("Password Error!");
            }

        } else {
            store.showerrormessage("Email Error!");
        }

    }//GEN-LAST:event_jTextField23MouseClicked


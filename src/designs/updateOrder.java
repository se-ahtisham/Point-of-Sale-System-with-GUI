    private void orderidKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_orderidKeyPressed
        // TODO add your handling code here:
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            if (!orderid.getText().equals("Search by Order-ID") && !orderid.getText().equals(" ")) {
                Order obj = store.searchOrder(Integer.parseInt(orderid.getText()));
                if (obj != null) {
                    this.setSinglevalueontabelorder(obj);
                }
            } else {
                store.showerrormessage("Search User");
            }
        }
        if (evt.getKeyCode() == KeyEvent.VK_BACK_SPACE) {
            addvaluesorder();
        }

    }//GEN-LAST:event_orderidKeyPressed

